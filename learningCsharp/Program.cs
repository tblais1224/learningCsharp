using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace learningCsharp
{
    class Program
    {
        static void Main(string[] args)
        {

            Console.WriteLine(Math.Pow(3.3, 30));  // gives number 3.3 raised to the power of 30
